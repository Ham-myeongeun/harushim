  /// 날짜 기반으로 "오늘의" 항목을 고릅니다 (하루 동안 같은 문구 유지).
  static String todays(List<String> array) {
    final now = DateTime.now();
    final dayOfYear = now.difference(DateTime(now.year, 1, 1)).inDays;
    return array[dayOfYear % array.length];
  }
